const selectedSoundReducer = (selectedSound = null, action) => {
  if (action.type === 'SOUND_SELECTED') {
    let audio = document.getElementById(action.payload.keyTrigger);
    audio.play();
    return action.payload;
  }

  return selectedSound;
};

